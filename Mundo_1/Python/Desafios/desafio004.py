algo = input('Digite algo:')

print('É apenas letras?', algo.isalpha())
print('É apenas números?', algo.isnumeric())
print('É letras e números?', algo.isalnum())
print('Está e, maiúsculas?', algo.isupper())
print('Está em minúsculas?', algo.islower())
print('São só espaços?', algo.isspace())
print('Está capitalizada?', algo.istitle()) #Nome proprio


